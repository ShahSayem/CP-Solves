#include <bits/stdc++.h>
using namespace std;

void solve()
{
    long long n;
    cin>>n; 

    for (int i = 0; i < n; i++){
        for (int j = 0; j < n; j++){
            cout<<"*";
        }
        cout<<endl;
    }
    
    
}
int main()
{
    int t;
    cin>>t;

    while (t--){
        solve();
        cout<<endl;     
    }
    
    return 0;
}
    